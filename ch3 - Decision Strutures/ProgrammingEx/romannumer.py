# Roman number

print("Enter number(1 - 10):")

num = int(input())

if num == 1:
    print("I")

else:
    if num == 2:
        print("II")

    else:
        if num == 3:
            print("III")

        else:
            if num == 4:
                print("IV")

            else:
                if num == 5:
                    print("V")

                else:
                    if num == 6:
                        print("VI")

                    else:
                        if num == 7:
                            print("VII")

                        else:
                            if num == 8:
                                print("VIII")

                            else:
                                if num == 9:
                                    print("IX")

                                else:
                                    if num == 10:
                                        print("X")

                                    else:
                                        print("Error.")