# 5. Distance Traveled
# Assuming there are no accidents or delays, the distance that a car travels down the interstate
# can be calculated with the following formula:
# Distance = Speed * Time
# A car is traveling at 70 miles per hour. Write a program that displays the following:
# • The distance the car will travel in 6 hours
# • The distance the car will travel in 10 hours
# • The distance the car will travel in 15 hours

top_speed = 70
#Distance that the car will travel in 6 hours
distance6 = top_speed * 6

#Distance that the car will travel in 10 hrs
distance10 = top_speed * 10

#Distance that the car will travel in 15 hrs

distance15 = top_speed * 15

print("The distance the car will travel: ", distance6)
print("The distance the car will travel: ", distance10)
print("The distance the car will travel: ", distance15)