# Distance Traveled
# Assuming there are no accidents or delays, the distance that a car travels down the interstate
# can be calculated with the following formula:
# Distance = Speed X Time
# A car is traveling at 70 miles per hour. Write a program that displays the following:
# • The distance the car will travel in 6 hours
# • The distance the car will travel in 10 hours
# • The distance the car will travel in 15 hours

miles_per_hour = 70

travel_in_six_hour = miles_per_hour * 6
travel_in_ten_hour = miles_per_hour * 10
travel_in_fifteen = miles_per_hour * 15

print(f"Distance the car will travel in 6 hours: {travel_in_six_hour} miles")
print(f"Distance the car will travel in 10 hours: {travel_in_ten_hour} miles")
print(f"Distance the car will travel in 15 hours: {travel_in_fifteen} miles")
